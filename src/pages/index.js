import { useState, useEffect } from 'react'
import Head from 'next/head'
import styles from '../styles/Home.module.scss'
import Bio from '../components/bio/Bio'
import Post from '../components/Post'
import { useAuth } from '../hooks/useAuth.js'
import PostForm from '../components/PostForm/PostForm'
import netlifyIdentity from 'netlify-identity-widget'
import { getAllPosts, createPost } from '../lib/posts'

export default function Home({ posts: defaultPosts }) {
  const [posts, updatePosts] = useState(defaultPosts)

  const postsSorted = posts.sort(function (a, b) {
    return new Date(b.date) - new Date(a.date)
  })

  const { user, logIn, logOut } = useAuth()

  async function handleOnSubmit(data, e) {
    e.preventDefault()

    await createPost(data)

    const response = await fetch(
      `https://api.airtable.com/v0/${process.env.AIRTABLE_BASE_ID}/Posts`,
      {
        method: 'POST',
        body: JSON.stringify(data),
      }
    )

    const posts = await getAllPosts()
    updatePosts(posts)
  }

  return (
    <div className={styles.container}>
      <Head>
        <title>NextJS Blog App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      {/* <button onClick={logIn}>Log In</button> */}
      {!user && (
        <button
          onClick={() => {
            netlifyIdentity.open()
          }}
        >
          Log In
        </button>
      )}
      {user && <button onClick={logOut}>Log Out</button>}

      <main className={styles.main}>
        <Bio
          headshot="https://avatars.githubusercontent.com/u/94413896?v=4"
          name="Angela Yang"
          tagline="Fullstack Software Developer"
        />

        <ul className={styles.posts}>
          {postsSorted.map((post) => {
            const { content, id, date } = post
            return (
              <li key={id}>
                <Post
                  content={content}
                  date={new Intl.DateTimeFormat('en-US', {
                    dateStyle: 'short',
                    timeStyle: 'short',
                  }).format(new Date(date))}
                  // date={new Intl.DateTimeFormat('en-US').format(new Date(date))}
                />
              </li>
            )
          })}
        </ul>

        {user && <PostForm onSubmit={handleOnSubmit} />}
      </main>
    </div>
  )
}

export async function getStaticProps() {
  // const posts = await getAllPosts()
  return {
    props: {
      posts: [],
    },
  }
}
